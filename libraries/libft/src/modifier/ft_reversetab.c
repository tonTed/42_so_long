#include "libft.h"

void	ft_reversetab(int **tab, size_t len)
{
	size_t	i;

	i = 0;
	while (i < len)
	{
		ft_swap(&(*tab)[i], &(*tab)[len - 1]);
		i++;
		len--;
	}
}
